
public class GameData
{
    private int levelPass;
    private int currentLevel;
    private int lightBulb;
    private int session;

    public int LevelPass
    {
        get
        {
            return levelPass;
        }
        set
        {
            levelPass = value;
        }
    }
    public int CurrentLevel
    {
        get
        {
            return currentLevel;
        }
        set
        {
            currentLevel = value;
        }
    }
    public int LightBulb
    {
        get
        {
            return lightBulb;
        }
        set
        {
            lightBulb = value;
        }
    }
    public int Session
    {
        get
        {
            return session;
        }
        set
        {
            session = value;
        }
    }
}
